import turtle

# კვადრატი 1: (-300, 100)
turtle.penup()
turtle.goto(-300, 100)
turtle.pendown()
turtle.forward(200)  # ქვედა გვერდი
turtle.left(90)
turtle.forward(200)  # მარჯვენა გვერდი
turtle.left(90)
turtle.forward(200)  # ზედა გვერდი
turtle.left(90)
turtle.forward(200)  # მარცხენა გვერდი
turtle.left(90)

